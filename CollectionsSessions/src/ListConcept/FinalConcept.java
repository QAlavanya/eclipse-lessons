package ListConcept;

public class FinalConcept {

	// final is a keyword is used to define the constant values
	public static void main(String[] args) {

		int i = 10;
		i = 20;
		i = 30;
		System.out.println(i);

		// but if we give final i=10 then i can't change the value of i

		final int j = 10; // used to define the constant values
		// j = 20;
		// final keyword is also used to prevent inheritance

	}

}
